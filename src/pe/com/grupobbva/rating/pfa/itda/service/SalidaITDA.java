public class SalidaITDA {
	
	private List<Ejercicio> ejercicioIndividual;
	private List<Ejercicio> ejercicioGrupal;
	
	
	public List<Ejercicio> getEjercicioIndividual() {
		return ejercicioIndividual;
	}
	public void setEjercicioIndividual(List<Ejercicio> ejercicioIndividual) {
		this.ejercicioIndividual = ejercicioIndividual;
	}
	public List<Ejercicio> getEjercicioGrupal() {
		return ejercicioGrupal;
	}
	public void setEjercicioGrupal(List<Ejercicio> ejercicioGrupal) {
		this.ejercicioGrupal = ejercicioGrupal;
	}
	
	

}
