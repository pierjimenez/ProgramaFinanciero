package pe.com.grupobbva.rating.itcu;

public class InputITUC {
	
	private String codigoCliente;
	private String anioEjercicio;
	private String mesEjercicio;
	private String tipoEmpresa;
	private String codigoAnalisis;
	private String modelo;
	private String opcion;
	

	public String getCodigoCliente() {
		return codigoCliente;
	}
	public void setCodigoCliente(String codigoCliente) {
		this.codigoCliente = codigoCliente;
	}
	public String getAnioEjercicio() {
		return anioEjercicio;
	}
	public void setAnioEjercicio(String anioEjercicio) {
		this.anioEjercicio = anioEjercicio;
	}
	public String getMesEjercicio() {
		return mesEjercicio;
	}
	public void setMesEjercicio(String mesEjercicio) {
		this.mesEjercicio = mesEjercicio;
	}
	public String getTipoEmpresa() {
		return tipoEmpresa;
	}
	public void setTipoEmpresa(String tipoEmpresa) {
		this.tipoEmpresa = tipoEmpresa;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getOpcion() {
		return opcion;
	}
	public void setOpcion(String opcion) {
		this.opcion = opcion;
	}
	public String getCodigoAnalisis() {
		return codigoAnalisis;
	}
	public void setCodigoAnalisis(String codigoAnalisis) {
		this.codigoAnalisis = codigoAnalisis;
	}

	

	
}

